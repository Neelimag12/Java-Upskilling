package com.upskilling;

public class TypeCasting {
	public static void main(String[] args) {
		int i = 6;
		float f = (int)i;
		System.out.println(f);
		double d = 789;
		byte b = (byte)d;
		System.out.println(d);
		float g=8.0f;
		int n = (int)g;
		System.out.println(n);
		int s=12;
		byte b1=(byte)s;
		System.out.println(b1);
		byte b2=12;
		int a =b2;
		System.out.println(a);
	}

}
